/**
 * 10 уровней сложения, по 20 заданий в каждом.
 * Формат: question | correct | options[0] | options[1] | options[2]
 */
export interface AdditionTask {
  question: string;
  correct: number;
  options: number[];
}

export interface AdditionLevel {
  level: number;
  tasks: AdditionTask[];
}

/** correct + два неверных варианта; в options всегда включаем правильный ответ */
function t(q: string, correct: number, wrong1: number, wrong2: number): AdditionTask {
  return { question: q, correct, options: [correct, wrong1, wrong2] };
}

